/**
 * Generates Lucide icon registries:
 * - lib/lucide/subpage-icon-registry.ts (SubPageHero / DynamicIcon — minimal)
 * - lib/lucide/icon-picker-registry.ts (IconSelect — full curated list)
 *
 * Run: node scripts/generate-lucide-registry.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import * as Lucide from 'lucide-react';

const ROOT = process.cwd();
const ICONS_SOURCE = path.join(ROOT, 'lib/constants/lucide-icons.ts');
const SUBPAGE_OUTPUT = path.join(ROOT, 'lib/lucide/subpage-icon-registry.ts');
const PICKER_OUTPUT = path.join(ROOT, 'lib/lucide/icon-picker-registry.ts');

const src = fs.readFileSync(ICONS_SOURCE, 'utf8');

const pickerNames = new Set();

const extraMatch = src.match(/EXTRA_DYNAMIC_ICONS = (\[[\s\S]*?\]) as const/);
if (extraMatch) {
  for (const match of extraMatch[1].matchAll(/'([A-Z][A-Za-z0-9]*)'/g)) {
    pickerNames.add(match[1]);
  }
}

for (const [, block] of src.matchAll(/icons:\s*\[([\s\S]*?)\]/g)) {
  for (const match of block.matchAll(/'([A-Z][A-Za-z0-9]*)'/g)) {
    pickerNames.add(match[1]);
  }
}

/** Icons referenced by SubPageHero pages (badgeIcon + stats) */
const subpageNames = new Set();

if (extraMatch) {
  for (const match of extraMatch[1].matchAll(/'([A-Z][A-Za-z0-9]*)'/g)) {
    subpageNames.add(match[1]);
  }
}

for (const file of walkTsx(ROOT)) {
  const rel = path.relative(ROOT, file).replace(/\\/g, '/');
  if (!rel.startsWith('app/') || !file.endsWith('.tsx')) continue;

  const page = fs.readFileSync(file, 'utf8');
  if (!page.includes('SubPageHero')) continue;

  for (const match of page.matchAll(/badgeIcon="([A-Z][A-Za-z0-9]*)"/g)) {
    subpageNames.add(match[1]);
  }
  for (const match of page.matchAll(/icon:\s*'([A-Z][A-Za-z0-9]*)'/g)) {
    subpageNames.add(match[1]);
  }
}

function walkTsx(dir) {
  const files = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (entry.name === 'node_modules' || entry.name === '.next') continue;
      files.push(...walkTsx(full));
    } else if (entry.name.endsWith('.tsx')) {
      files.push(full);
    }
  }
  return files;
}

function buildRegistryFile({ outputPath, title, names }) {
  const sorted = [...names].sort();
  const valid = sorted.filter(name => name in Lucide);
  const missing = sorted.filter(name => !(name in Lucide));

  if (missing.length > 0) {
    console.warn(`[${path.basename(outputPath)}] skipping invalid:`, missing.join(', '));
  }

  const importBlock = valid.join(',\n  ');
  const registryBlock = valid.map(name => `  ${name},`).join('\n');
  const registryConst =
    outputPath.includes('subpage') ? 'SUBPAGE_ICON_REGISTRY' : 'ICON_PICKER_REGISTRY';
  const typeName =
    outputPath.includes('subpage') ? 'SubPageLucideIconName' : 'IconPickerLucideIconName';
  const guardName =
    outputPath.includes('subpage') ? 'isSubPageLucideIconName' : 'isIconPickerLucideIconName';

  const file = `/**
 * ${title}
 * @generated by scripts/generate-lucide-registry.mjs — do not edit manually
 */
'use client';

import type { LucideIcon } from 'lucide-react';
import {
  ${importBlock},
} from 'lucide-react';

export const ${registryConst} = {
${registryBlock}
} as const satisfies Record<string, LucideIcon>;

export type ${typeName} = keyof typeof ${registryConst};

export function ${guardName}(name: string): name is ${typeName} {
  return name in ${registryConst};
}
`;

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, file);
  console.log(`Wrote ${valid.length} icons to ${path.relative(ROOT, outputPath)}`);
}

buildRegistryFile({
  outputPath: SUBPAGE_OUTPUT,
  title: 'Minimal Lucide set for SubPageHero and DynamicIcon',
  names: subpageNames,
});

buildRegistryFile({
  outputPath: PICKER_OUTPUT,
  title: 'Full curated Lucide set for IconSelect (admin icon picker)',
  names: pickerNames,
});
